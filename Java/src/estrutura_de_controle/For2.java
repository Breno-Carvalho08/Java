package estrutura_de_controle;

public class For2 {	
	public static void main(String[] args) {
		int cont = 10;
		int cont2 = 0;
		
		//Crescente
		for(int x = 1; x <= cont; x++) {
			System.out.println(x);
		}
		 
		//Decrescente
		for(int y = 10; y >= cont2; y--) {
			System.out.println(y);
		}
		
	}
}
